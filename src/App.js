const soundsName = {
  heatherKit: "Heather Kit",
  smoothPianoKit: "Smooth Piano Kit"
}
const soundsGroup = {
  heatherKit: firstSoundsGroup,
  smoothPianoKit: secondSoundsGroup
}


const App = () => {
  const [power, setPower] = React.useState(true)
  const [soundName, setSoundName] = React.useState("")
  const [soundType, setSoundType] = React.useState("heatherKit")
  const [sounds, setSounds] = React.useState(soundsGroup[soundType])


  const styleActiveKey = (audio) => {
    audio.parentElement.style.background = "linear-gradient(0deg, rgb(130, 130, 130), rgb(200, 200, 200))"
  }

  const deactivateAudio = (audio) => {
    setTimeout(() => {
      audio.parentElement.style.background = "linear-gradient(0deg, rgb(160, 160, 160), rgb(230, 230, 230))"
    }, 200)
  }

  const play = (key, sound) => {
    setSoundName(sound)
    const audio = document.getElementById(key)

    styleActiveKey(audio)
    audio.currentTime = 0
    audio.play()
    deactivateAudio(audio)
  }

  const stop = () => {
    setPower(!power)
  }


  return (
    <div id="drum-machine">
      <div className="wrapper">
        <Keyboard power={power} play={play} sounds={sounds} />

        <DrumControl 
          name={soundName || soundsName[soundType]} 
          power={power}
          stop={stop} />
      </div>
    </div>
  )
}


export default App
